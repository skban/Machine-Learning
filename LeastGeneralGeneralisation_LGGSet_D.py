def LGGSet(d):
    j=0
    instances = d
    instances_len = len(instances)
    x= instances[j]
    print("############################################################################################################")
    h=x
    print(j, "x", x,"(Conjunction)","h", h)
    print("############################################################################################################")
    instances_left = instances_len -1
    while j<instances_left:
        j = j+1
        #print(j)
        x = instances[j]
        if len(LGGConj(h,x)) > 0:
            print("######################################################################################################")
            print(j, "Conjunction of x and h")
            #print("x", x)
            print("h", h)
            h = LGGConj(h,x)
            print("------------------------------------------------------------------------------------------------------")
            print("LGGConj(h,x)-->")
            print("------------------------------------------------------------------------------------------------------")
            print(h)
            print("######################################################################################################")
            #instances_left = instances_left-1
    return h
